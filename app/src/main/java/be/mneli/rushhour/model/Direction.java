package be.mneli.rushhour.model;

/**
 * Direction can take 4 values:
 * <ul>
 * <li>LEFT for movement to the left</li>
 * <li>RIGHT for movement to the right</li>
 * <li>UP for movement to up</li>
 * <li>DOWN for movement to down</li>
 * </ul>
 * Created by mneli on 05/07/17.
 */

public enum Direction {
    /**
     * for movement to the left
     */
    LEFT,
    /**
     * for movement to the right
     */
    RIGHT,
    /**
     * for movement to up
     */
    UP,
    /**
     * for movement to down
     */
    DOWN;
}
